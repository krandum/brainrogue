char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*ss;

	ss = (char*)s;
	i = -1;
	while (s[++i])
		if (s[i] == c)
			return (&(ss[i]));
	if (!s[i] && !c)
		return (&(ss[i]));
	return (0);
}
